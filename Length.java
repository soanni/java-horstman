class Length{
	public static void main(String[] args) {
		final int a1[] = new int[10];
		final int a2[] = {1,2,3,4,5};
		final int a3[] = {1,2,3};

		System.out.println("a1 length : " + a1.length);
		System.out.println("a2 length : " + a2.length);
		System.out.println("a3 length : " + a3.length);
	}
}